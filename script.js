//calculate basic arithmetic operations
function add(num1, num2) {
    let sum = num1 + num2;
    return sum;
}

function subtract(num1, num2) {
    let minus = num1 - num2;
    return minus; 
}

function multiply(num1, num2) {
    let mul = num1 * num2;
    return mul; 
}

function divide(num1, num2) {
    let div = num1 / num2;
    return div; 
}